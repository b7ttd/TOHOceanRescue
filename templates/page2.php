			<h4>The Performance Test</h4>
			The Performance Test will consist of: (1) Entrance into 
			the water with a standard shallow dive,followed by a 
			minimum speed test in freestyle - 50 yards in 35 seconds 
			or less,(2) An endurance swim of 200 yards in continuous 
			crawl style in good form (body must be planing the water 
			continuously), within 3:45, (3) Front surface approach 
			for 25 yards, followed by an alternate cross chest carry 
			of a struggling victim for 25 yards, (4) Front and back 
			head hold escapes, (5) 15 yard approach with a piece of 
			equipment (torp or rescue buoy), place equipment on 
			victim and return to starting location. (6) Surface dive 
			in a minimum of 10 feet of water to retrieve a 10 pound 
			weight and bring it to the surface, (7) Witness a 
			demonstration of handling neck and back injuries in 
			shallow and deep water, (8) Written test on lifeguarding.
